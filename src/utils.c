#include "minishell.h"

int	is_sep(char c)
{
	if (c == '|')
		return (1);
	return (0);
}

int	is_white(char c)
{
	if (c == ' ' || c == '\r' || c == '\t' || c == '\n'
		|| c == '\v' || c == '\f')
		return (1);
	return (0);
}
